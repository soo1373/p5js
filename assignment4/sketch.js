function setup() {
  createCanvas(600, 400);
  noLoop(); 
}

function draw() {
  background(255, 235, 235); // 연한 분홍색 배경

  // 1. 뒷머리 수정 (정수리부터 둥글게 시작되는 직모 단발)
  noStroke();
  fill(45, 30, 20); // 짙은 밤색
  // 정수리 쪽 볼륨 (곡선이 위에서부터 시작)
  ellipse(300, 170, 230, 210);
  // 아래로 떨어지는 매끄러운 단발 끝단
  rect(185, 170, 230, 150, 0, 0, 20, 20); 

  // 2. 목
  fill(255, 220, 190);
  rect(280, 280, 40, 40);

  // 3. 상체 (옷)
  fill(240, 100, 130); // 진분홍색 상의
  // 어깨 라인을 둥글게 처리
  rect(170, 320, 260, 100, 60, 60, 0, 0);

  // 4. 얼굴 (동그랗고 매끈하게)
  fill(255, 224, 189); // 살구색 피부
  ellipse(300, 200, 190, 200);

  // 5. 귀와 귀걸이
  fill(255, 224, 189);
  circle(205, 210, 25);
  circle(395, 210, 25);
  fill(255, 215, 0); // 금색 귀걸이
  circle(205, 220, 8);
  circle(395, 220, 8);

  // 6. 앞머리 수정 
  fill(45, 30, 20);
  arc(300, 170, 190, 150, PI, TWO_PI, CHORD);
  
  // 귀 옆으로 자연스럽게 떨어지는 애교머리
  rect(202, 170, 15, 50, 0, 0, 10, 10);
  rect(383, 170, 15, 50, 0, 0, 10, 10);

  // 7. 눈 
  stroke(45, 30, 20);
  strokeWeight(2);
  fill(255);
  circle(265, 210, 45); // 눈 흰자
  circle(335, 210, 45);
  
  noStroke();
  fill(45, 30, 20);
  circle(265, 212, 25); // 눈동자
  circle(335, 212, 25);
  
  fill(255);
  circle(272, 205, 10); // 눈 하이라이트
  circle(342, 205, 10);

  // 8. 볼 홍조 
  fill(255, 180, 180, 150);
  ellipse(245, 250, 40, 20);
  ellipse(355, 250, 40, 20);

  // 9. 코와 입
  stroke(200, 140, 120);
  strokeWeight(2);
  noFill();
  // 작은 코
  arc(300, 245, 10, 6, 0, PI);
  // 웃는 입
  stroke(220, 80, 80);
  arc(300, 275, 30, 20, 0.2, PI - 0.2);
}